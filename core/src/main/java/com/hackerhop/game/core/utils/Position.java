package com.hackerhop.game.core.utils;

public class Position {
    private double x, y;

    public Position(double x, double y) {
        setPosition(x, y);
    }

    public void setPosition(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double setX() {
        return x;
    }

    public double setY() {
        return y;
    }
}
